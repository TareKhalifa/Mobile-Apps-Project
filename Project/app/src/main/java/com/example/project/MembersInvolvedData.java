package com.example.project;
public class MembersInvolvedData {
    private String name;
    private String img;
    public MembersInvolvedData(String name, String img) {
        this.name = name;
        this.img = img;
    }
    public String getName() {
        return name;
    }
    public String getImg() {
        return img;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setImg(String img) {
        this.img = img;
    }

}